#Diseño de un algoritmo que lea 3 números y calcula su promedio

print('algoritmo que lee 3 números y calcula su promedio')

nombre = input('Ingresa tu nombre y oprime enter ')

a = int(input('Ingrese el primer número: '))
b = int(input('Ingrese el segundo número: '))
c = int(input('Ingrese el tercer  número: '))

suma = a + b + c

promedio = suma / 3

print('El promedio es:  ' + str(promedio))

print('Muchas gracias por tu\nparticipación  '+nombre)


