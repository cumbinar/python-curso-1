'''El método split() divide una cadena en subcadenas 
y las devuelve almacenadas en una lista.'''

cadena = input('Ingrese una oración corta: ')
#Cumbitara es la tierra de los andes
print()
print(cadena.split(" ")) #el espacio es el parámetro para dividir
#['Cumbitara', 'es', 'la', 'tierra', 'de', 'los', 'andes'] 


